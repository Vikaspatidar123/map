const useGetRates = () => {
	const newRates = countries.map((item) => ({
		heading : item.name,
		point   : [item.latitude, item.longitude],
		price   : rates?.[item.country_code] || 0,
	})).filter((item) => item.price);
	return {
		newRates,
	};
};

